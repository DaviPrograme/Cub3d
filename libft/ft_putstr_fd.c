#include "libft.h"

int	ft_putstr_fd(char *s, int fd)
{
	int cont;

	cont = 0;
	if (s)
	{
		while (s[cont] != '\0')
		{
			ft_putchar_fd(s[cont], fd);
			++cont;
		}
	}
	return (1);
}
